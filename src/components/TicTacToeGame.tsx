import React, { useState, useEffect } from 'react';
import {
  useCreateTicTacToeGameMutation,
  useGetCurrentTicTacToeGameQuery,
  useMakeTicTacToeMoveMutation
} from '../features/user/userApi';

interface TicTacToeGameProps {
  isOpen: boolean;
  onClose: () => void;
  onRewardReceived?: () => void;
}

const TicTacToeGame: React.FC<TicTacToeGameProps> = ({ isOpen, onClose, onRewardReceived }) => {
  console.log('TicTacToeGame: –ö–æ–º–ø–æ–Ω–µ–Ω—Ç —Ä–µ–Ω–¥–µ—Ä–∏—Ç—Å—è —Å –ø—Ä–æ–ø—Å–∞–º–∏:', { isOpen, hasOnClose: !!onClose, hasOnRewardReceived: !!onRewardReceived });

  if (isOpen) {
    console.log('TicTacToeGame: –ò–ì–†–ê –î–û–õ–ñ–ù–ê –ë–´–¢–¨ –û–¢–ö–†–´–¢–ê!');
  }

  const [message, setMessage] = useState('');
  const [selectedCell, setSelectedCell] = useState<number | null>(null);
  const [showResult, setShowResult] = useState(false);
  const [gameResult, setGameResult] = useState<string | null>(null);
  const [isProcessingResult, setIsProcessingResult] = useState(false);

  const { data: currentGameData, refetch: refetchCurrentGame } = useGetCurrentTicTacToeGameQuery(undefined, {
    skip: !isOpen,
  });

  const [createGame, { isLoading: isCreatingGame }] = useCreateTicTacToeGameMutation();
  const [makeMove, { isLoading: isMoving }] = useMakeTicTacToeMoveMutation();

  const game = currentGameData?.game;
  const canPlay = currentGameData?.canPlay ?? true;

  console.log('TicTacToeGame: –î–∞–Ω–Ω—ã–µ –∏–≥—Ä—ã:', {
    currentGameData,
    game,
    canPlay,
    hasGame: !!game,
    gameState: game?.game_state
  });

  useEffect(() => {
    if (isOpen) {
      refetchCurrentGame();
      setShowResult(false);
      setGameResult(null);
      setMessage('');
      setIsProcessingResult(false);
    }
  }, [isOpen, refetchCurrentGame]);

  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∑–∞–≤–µ—Ä—à–µ–Ω–∞ –ª–∏ –∏–≥—Ä–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –¥–∞–Ω–Ω—ã—Ö
  useEffect(() => {
    if (game && game.game_state?.status === 'finished' && !showResult && !isProcessingResult) {
      console.log('TicTacToeGame: –û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω–∞—è –∏–≥—Ä–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –¥–∞–Ω–Ω—ã—Ö', {
        result: game.result,
        winner: game.game_state.winner
      });

      setIsProcessingResult(true);

      // –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–µ—Ä–∂–∫—É –¥–ª—è –≤—Å–µ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ
      setTimeout(() => {
        setGameResult(game.result);
        setShowResult(true);
        setIsProcessingResult(false);

        // –ï—Å–ª–∏ –ø–æ–±–µ–¥–∞, –≤—ã–∑—ã–≤–∞–µ–º callback
        if (game.result === 'win' && onRewardReceived) {
          console.log('TicTacToeGame: –ü–æ–±–µ–¥–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ! –í—ã–∑—ã–≤–∞–µ–º onRewardReceived —á–µ—Ä–µ–∑ 3 —Å–µ–∫—É–Ω–¥—ã...');
          setTimeout(() => {
            console.log('TicTacToeGame: –í—ã–∑—ã–≤–∞–µ–º onRewardReceived —Å–µ–π—á–∞—Å (–∏–∑ –∑–∞–≥—Ä—É–∑–∫–∏)!');
            onRewardReceived();
          }, 3000);
        }
      }, 1500); // –ó–∞–¥–µ—Ä–∂–∫–∞ 1.5 —Å–µ–∫—É–Ω–¥—ã –¥–ª—è –≤—Å–µ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ
    }
  }, [game, showResult, isProcessingResult, onRewardReceived]);



  const handleStartNewGame = async () => {
    console.log('TicTacToeGame: –ù–∞—á–∏–Ω–∞–µ–º —Å–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–π –∏–≥—Ä—ã...');
    setShowResult(false);
    setGameResult(null);
    setIsProcessingResult(false);
    try {
      console.log('TicTacToeGame: –í—ã–∑—ã–≤–∞–µ–º createGame()...');
      const result = await createGame().unwrap();
      console.log('TicTacToeGame: –†–µ–∑—É–ª—å—Ç–∞—Ç —Å–æ–∑–¥–∞–Ω–∏—è –∏–≥—Ä—ã:', result);
      if (result.success) {
        console.log('TicTacToeGame: –ò–≥—Ä–∞ —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω–∞');
        setMessage('');
        refetchCurrentGame();
      }
    } catch (error: any) {
      console.error('TicTacToeGame: –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∏–≥—Ä—ã:', error);
      setMessage(error?.data?.error || '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∏–≥—Ä—ã');
    }
  };

  const handleCellClick = async (position: number) => {
    if (!game || game.game_state.status !== 'playing' || game.game_state.currentPlayer !== 'player') {
      return;
    }

    if (game.game_state.board[position] !== null) {
      return;
    }

    setSelectedCell(position);

    try {
      const result = await makeMove({ position }).unwrap();
      if (result.success) {
        setMessage(result.message || '');
        refetchCurrentGame();

        // –ï—Å–ª–∏ –∏–≥—Ä–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        if (result.game?.game_state?.status === 'finished') {
          console.log('TicTacToeGame: –ò–≥—Ä–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!', {
            result: result.game.result,
            gameState: result.game.game_state,
            winner: result.game.game_state.winner
          });

          setIsProcessingResult(true);

          // –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–µ—Ä–∂–∫—É –¥–ª—è –≤—Å–µ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
          setTimeout(() => {
            setGameResult(result.game.result);
            setShowResult(true);
            setIsProcessingResult(false);

            // –ï—Å–ª–∏ –ø–æ–±–µ–¥–∞, –≤—ã–∑—ã–≤–∞–µ–º callback —á–µ—Ä–µ–∑ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–µ –≤—Ä–µ–º—è
            if (result.game?.result === 'win' && onRewardReceived) {
              console.log('TicTacToeGame: –ü–æ–±–µ–¥–∞! –í—ã–∑—ã–≤–∞–µ–º onRewardReceived —á–µ—Ä–µ–∑ 3 —Å–µ–∫—É–Ω–¥—ã...');
              setTimeout(() => {
                console.log('TicTacToeGame: –í—ã–∑—ã–≤–∞–µ–º onRewardReceived —Å–µ–π—á–∞—Å!');
                onRewardReceived();
              }, 3000); // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç 3 —Å–µ–∫—É–Ω–¥—ã
            } else if (result.game?.result === 'win') {
              console.log('TicTacToeGame: –ü–æ–±–µ–¥–∞, –Ω–æ onRewardReceived –Ω–µ –ø–µ—Ä–µ–¥–∞–Ω');
            } else {
              console.log('TicTacToeGame: –†–µ–∑—É–ª—å—Ç–∞—Ç –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –ø–æ–±–µ–¥–æ–π:', result.game?.result);
            }
          }, 1500); // –ó–∞–¥–µ—Ä–∂–∫–∞ 1.5 —Å–µ–∫—É–Ω–¥—ã –¥–ª—è –≤—Å–µ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
        }
      }
    } catch (error: any) {
      setMessage(error?.data?.error || '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–≤–µ—Ä—à–µ–Ω–∏–∏ —Ö–æ–¥–∞');
    } finally {
      setSelectedCell(null);
    }
  };

  const getCellContent = (index: number) => {
    if (!game) return '';
    const cell = game.game_state.board[index];
    if (cell === 'X') return '‚úñÔ∏è';
    if (cell === 'O') return '‚≠ï';
    return '';
  };

  const getCellStyle = (index: number) => {
    const baseStyle = "w-20 h-20 border-2 border-gray-600 bg-gray-800 hover:bg-gray-700 transition-colors duration-200 flex items-center justify-center text-2xl font-bold cursor-pointer";

    if (!game || game.game_state.board[index] !== null) {
      return baseStyle + " cursor-not-allowed opacity-75";
    }

    if (game.game_state.currentPlayer !== 'player' || game.game_state.status !== 'playing') {
      return baseStyle + " cursor-not-allowed";
    }

    if (selectedCell === index) {
      return baseStyle + " bg-blue-600";
    }

    return baseStyle;
  };

  const getStatusMessage = () => {
    if (!game) return '';

    if (game.game_state.status === 'finished') {
      if (game.game_state.winner === 'player') {
        return 'üéâ –í—ã –≤—ã–∏–≥—Ä–∞–ª–∏!';
      } else if (game.game_state.winner === 'bot') {
        return 'üòû –í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏';
      } else {
        return 'ü§ù –ù–∏—á—å—è';
      }
    }

    if (game.game_state.currentPlayer === 'player') {
      return 'üéÆ –í–∞—à —Ö–æ–¥';
    } else {
      return 'ü§ñ –•–æ–¥ –±–æ—Ç–∞...';
    }
  };

  if (!isOpen) {
    console.log('TicTacToeGame: –ù–µ –æ—Ç–∫—Ä—ã—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º null');
    return null;
  }
  console.log('TicTacToeGame: –†–µ–Ω–¥–µ—Ä–∏–º –∫–æ–º–ø–æ–Ω–µ–Ω—Ç –∏–≥—Ä—ã');

  return (
    <div
      className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center"
      style={{ zIndex: 9999 }}
      onClick={(e) => {
        if (e.target === e.currentTarget) {
          console.log('TicTacToeGame: –ö–ª–∏–∫ –ø–æ —Ñ–æ–Ω—É, –∑–∞–∫—Ä—ã–≤–∞–µ–º –∏–≥—Ä—É');
          onClose();
        }
      }}
    >
      <div
        className="bg-gray-900 rounded-lg p-6 max-w-md w-full mx-4 border border-gray-700"
      >
        <div className="flex justify-between items-center mb-6">
          <h2 className="text-2xl font-bold text-white">–ö—Ä–µ—Å—Ç–∏–∫–∏-–Ω–æ–ª–∏–∫–∏</h2>
          <button
            onClick={onClose}
            className="text-gray-400 hover:text-white text-2xl"
          >
            ‚úï
          </button>
        </div>

        {!canPlay ? (
          <div className="text-center">
            <p className="text-red-400 mb-4">–£ –≤–∞—Å –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å –ø–æ–ø—ã—Ç–∫–∏ –Ω–∞ —Å–µ–≥–æ–¥–Ω—è!</p>
            <p className="text-gray-400 mb-6">–ü—Ä–∏—Ö–æ–¥–∏—Ç–µ –∑–∞–≤—Ç—Ä–∞ –∑–∞ –Ω–æ–≤—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏.</p>
            <button
              onClick={onClose}
              className="px-6 py-2 bg-gray-700 text-white rounded hover:bg-gray-600 transition-colors"
            >
              –ó–∞–∫—Ä—ã—Ç—å
            </button>
          </div>
        ) : showResult ? (
          <div className="text-center">
            {/* –†–µ–∑—É–ª—å—Ç–∞—Ç –∏–≥—Ä—ã */}
            <div className="mb-6">
              {gameResult === 'win' && (
                <div>
                  <div className="text-6xl mb-4">üéâ</div>
                  <h3 className="text-2xl font-bold text-green-400 mb-2">–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º!</h3>
                  <p className="text-white mb-4">–í—ã –≤—ã–∏–≥—Ä–∞–ª–∏ –∏ –ø–æ–ª—É—á–∏–ª–∏ –±–æ–Ω—É—Å–Ω—ã–π –∫–µ–π—Å!</p>
                </div>
              )}
              {gameResult === 'lose' && (
                <div>
                  <div className="text-6xl mb-4">üòû</div>
                  <h3 className="text-2xl font-bold text-red-400 mb-2">–ü–æ—Ä–∞–∂–µ–Ω–∏–µ</h3>
                  <p className="text-white mb-4">–í —ç—Ç–æ—Ç —Ä–∞–∑ –Ω–µ –ø–æ–≤–µ–∑–ª–æ, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑!</p>
                </div>
              )}
              {gameResult === 'draw' && (
                <div>
                  <div className="text-6xl mb-4">ü§ù</div>
                  <h3 className="text-2xl font-bold text-yellow-400 mb-2">–ù–∏—á—å—è</h3>
                  <p className="text-white mb-4">–•–æ—Ä–æ—à–∞—è –∏–≥—Ä–∞! –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑!</p>
                </div>
              )}
            </div>

            {/* –§–∏–Ω–∞–ª—å–Ω–∞—è –¥–æ—Å–∫–∞ —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ */}
            {game && game.game_state && (
              <div className="mb-6">
                <p className="text-gray-300 mb-3 text-sm">–§–∏–Ω–∞–ª—å–Ω–∞—è –¥–æ—Å–∫–∞:</p>
                <div className="grid grid-cols-3 gap-2 mx-auto w-fit mb-4">
                  {Array.from({ length: 9 }, (_, index) => (
                    <div
                      key={index}
                      className="w-16 h-16 border-2 border-gray-600 bg-gray-800 flex items-center justify-center text-xl font-bold"
                    >
                      {getCellContent(index)}
                    </div>
                  ))}
                </div>

                {/* –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–µ */}
                <div className="text-sm text-gray-400">
                  {game.game_state.winner === 'player' && (
                    <p>üéÆ –í—ã –∏–≥—Ä–∞–ª–∏ ‚úñÔ∏è –∏ –≤—ã–∏–≥—Ä–∞–ª–∏!</p>
                  )}
                  {game.game_state.winner === 'bot' && (
                    <p>ü§ñ –ë–æ—Ç –∏–≥—Ä–∞–ª ‚≠ï –∏ –≤—ã–∏–≥—Ä–∞–ª</p>
                  )}
                  {game.game_state.winner === 'draw' && (
                    <p>ü§ù –ù–∏—á—å—è - –Ω–∏–∫—Ç–æ –Ω–µ –≤—ã–∏–≥—Ä–∞–ª</p>
                  )}
                </div>
              </div>
            )}

            <div className="flex gap-3">
              {game?.attempts_left && game.attempts_left > 0 && (
                <button
                  onClick={handleStartNewGame}
                  disabled={isCreatingGame}
                  className="flex-1 px-4 py-2 bg-green-600 text-white rounded hover:bg-green-700 disabled:opacity-50 transition-colors"
                >
                  {isCreatingGame ? '–°–æ–∑–¥–∞–Ω–∏–µ...' : '–ò–≥—Ä–∞—Ç—å –µ—â–µ'}
                </button>
              )}

              <button
                onClick={onClose}
                className="flex-1 px-4 py-2 bg-gray-700 text-white rounded hover:bg-gray-600 transition-colors"
              >
                –ó–∞–∫—Ä—ã—Ç—å
              </button>
            </div>

            {game?.attempts_left !== undefined && (
              <p className="text-sm text-gray-400 mt-4">
                –û—Å—Ç–∞–ª–æ—Å—å –ø–æ–ø—ã—Ç–æ–∫: {game.attempts_left}
              </p>
            )}
          </div>
        ) : isProcessingResult ? (
          <div className="text-center">
            {/* –≠–∫—Ä–∞–Ω –æ–∂–∏–¥–∞–Ω–∏—è –≤–æ –≤—Ä–µ–º—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ */}
            <div className="mb-6">
              <div className="text-4xl mb-4">‚è≥</div>
              <p className="text-white">–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç...</p>
            </div>
          </div>
        ) : !game || !game.game_state ? (
          <div className="text-center">
            <p className="text-gray-300 mb-6">
              –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∫—Ä–µ—Å—Ç–∏–∫–∏-–Ω–æ–ª–∏–∫–∏! –í—ã–∏–≥—Ä–∞–π—Ç–µ —É –±–æ—Ç–∞, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –±–æ–Ω—É—Å–Ω—ã–π –∫–µ–π—Å.
            </p>
            <p className="text-yellow-400 mb-4">–£ –≤–∞—Å –µ—Å—Ç—å 3 –ø–æ–ø—ã—Ç–∫–∏ –≤ –¥–µ–Ω—å</p>
            <button
              onClick={handleStartNewGame}
              disabled={isCreatingGame}
              className="px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 disabled:opacity-50 transition-colors"
            >
              {isCreatingGame ? '–°–æ–∑–¥–∞–Ω–∏–µ –∏–≥—Ä—ã...' : '–ù–∞—á–∞—Ç—å –∏–≥—Ä—É'}
            </button>
          </div>
        ) : (
          <div>
            {/* –ò–≥—Ä–æ–≤–æ–µ –ø–æ–ª–µ */}
            <div className="grid grid-cols-3 gap-2 mb-6 mx-auto w-fit">
              {Array.from({ length: 9 }, (_, index) => (
                <button
                  key={index}
                  onClick={() => handleCellClick(index)}
                  className={getCellStyle(index)}
                  disabled={isMoving || game.game_state.currentPlayer !== 'player' || game.game_state.status !== 'playing' || isProcessingResult}
                >
                  {getCellContent(index)}
                </button>
              ))}
            </div>

            {/* –°—Ç–∞—Ç—É—Å –∏–≥—Ä—ã */}
            <div className="text-center mb-4">
              <p className="text-lg font-semibold text-white mb-2">
                {isProcessingResult ? '‚è≥ –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç...' : getStatusMessage()}
              </p>

              <div className="flex justify-between text-sm text-gray-400">
                <span>–ü–æ–ø—ã—Ç–æ–∫ –æ—Å—Ç–∞–ª–æ—Å—å: {game.attempts_left}</span>
                <span>–í—ã –∏–≥—Ä–∞–µ—Ç–µ: ‚úñÔ∏è</span>
              </div>
            </div>

            {/* –°–æ–æ–±—â–µ–Ω–∏—è */}
            {message && !isProcessingResult && (
              <div className="mb-4 p-3 bg-gray-800 border border-gray-600 rounded text-center">
                <p className="text-white">{message}</p>
              </div>
            )}

            {/* –ö–Ω–æ–ø–∫–∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è */}
            <div className="flex gap-3">
              <button
                onClick={onClose}
                disabled={isProcessingResult}
                className="flex-1 px-4 py-2 bg-gray-700 text-white rounded hover:bg-gray-600 disabled:opacity-50 transition-colors"
              >
                –ó–∞–∫—Ä—ã—Ç—å
              </button>
            </div>

            {/* –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è */}
            {!isProcessingResult && (
              <div className="mt-4 p-3 bg-gray-800 border border-gray-600 rounded">
                <p className="text-xs text-gray-400 text-center">
                  –°–æ–±–µ—Ä–∏—Ç–µ 3 —Å–∏–º–≤–æ–ª–∞ –≤ —Ä—è–¥ (–ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏, –≤–µ—Ä—Ç–∏–∫–∞–ª–∏ –∏–ª–∏ –¥–∏–∞–≥–æ–Ω–∞–ª–∏), —á—Ç–æ–±—ã –≤—ã–∏–≥—Ä–∞—Ç—å!
                </p>
              </div>
            )}
          </div>
        )}
      </div>
    </div>
  );
};

export default TicTacToeGame;
